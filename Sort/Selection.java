/*Java Utilities*/
import java.io.*;
import java.util.*;

public class Selection
{
    public static void main(String args[])
    {
        /*Scanner from File Stuff*/
        Scanner inFile = null; 

        try 
            {
            // Create a scanner to read the file, file name is parameter
                inFile = new Scanner (new File("SortNums.txt"));
            } 
        catch (FileNotFoundException e) 
            {
            System.out.println ("File not found!");
            /*Stop Program if no file s found*/
            System.exit(0);
            }

        /*Arrays*/
        int[] originValues = new int[500];
        int[] sortValues = new int[500];

        /*Assign Original Values*/
        for (int i = 0; inFile.hasNext(); i++)
        {
            originValues[i] = inFile.nextInt();
            sortValues[i] = originValues[i];
        }

        /*Variables*/
        boolean allSorted = false;
        int tempValue = 0;

        for (int i = 0; i < sortValues.length - 1; i++)
        {
            int newIndex = i;

            /*Find Smallest*/
            for (int x = i + 1; x < sortValues.length; x++)
            {
                if (sortValues[x] < sortValues[newIndex])
                {
                    newIndex = x;
                }
            }

            /*Change to smaller Number*/
            tempValue = sortValues[newIndex];
            sortValues[newIndex] = sortValues[i];
            sortValues[i] = tempValue;
        }

        /*Print*/
        /*Header*/
        System.out.println("Original\tSorted");

        for (int z = 0; z < sortValues.length; z++)
        {
            System.out.println("  " + originValues[z] + "\t\t  " + sortValues[z]);
        }
    }
}

